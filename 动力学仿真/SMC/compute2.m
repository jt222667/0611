function y = compute2(q,dq,ddq)

%% 1. 定义符号变量
q1=q(1);
q2=q(2);
q3=q(3);
q4=q(4);
q5=q(5);
q6=q(6);
q7=q(7);

dq1=dq(1);
dq2=dq(2);
dq3=dq(3);
dq4=dq(4);
dq5=dq(5);
dq6=dq(6);
dq7=dq(7);

S1=ddq(1);
S2=ddq(2);
S3=ddq(3);
S4=ddq(4);
S5=ddq(5);
S6=ddq(6);
S7=ddq(7);

%% 2.D-H参数矩阵
R01 = [cos(q1) -sin(q1) 0; 0 0 -1; sin(q1) cos(q1) 0];
R12 = [cos(q2) -sin(q2) 0; 0 0 -1; sin(q2) cos(q2) 0];
R23 = [cos(q3) -sin(q3) 0; 0 0 1; -sin(q3) -cos(q3) 0];
R34 = [cos(q4) -sin(q4) 0; sin(q4) cos(q4) 0; 0 0 1];
R45 = [cos(q5) -sin(q5) 0; sin(q5) cos(q5) 0; 0 0 1];
R56 = [cos(q6) -sin(q6) 0; 0 0 -1; sin(q6) cos(q6) 0];
R67 = [cos(q7) -sin(q7) 0; 0 0 1; -sin(q7) -cos(q7) 0];

% 转置R
R10 = R01';
R21 = R12';
R32 = R23';
R43 = R34';
R54 = R45';
R65 = R56';
R76 = R67';

% 位置向量P
P01 = [0; -0.225; 0];
P12 = [0; -0.24; 0];
P23 = [0; 0.24; 0];
P34 = [0.56; 0; 0.2];
P45 = [0.5; 0; 0.195];
P56 = [0; -0.18; 0];
P67 = [0; 0.476; 0];

% 质心坐标PC
PC1 = [0.003; -0.067; -0.052];
PC2 = [0.001; 0.06; -0.058];
PC3 = [0.226; 0.005; -0.025];
PC4 = [0.238; 0.001; -0.001];
PC5 = [0.008; -0.028; -0.031];
PC6 = [-0.025; 0.023; 0.003];
PC7 = [0.003; -0.010; -0.199];

% 惯性张量IC
IC1 = [0.0980 -0.0050 -0.0030; -0.0050 0.0450 0.0220; -0.0030 0.0220 0.0720];
IC2 = [0.0870 -0.0050 -0.0020; -0.0050 0.0210 -0.0220; -0.0020 -0.0220 0.0690];
IC3 = [0.0790 -0.0020 -0.1200; -0.0020 0.5710 -0.0890; -0.1200 -0.0890 0.5300];
IC4 = [0.0320 -0.0010 -0.0520; -0.0010 0.3250 -0.0920; -0.0520 -0.0920 0.3130];
IC5 = [0.0140 -0.0110 -0.0010; -0.0110 0.0100 0.0020; -0.0010 0.0020 0.0110];
IC6 = [0.0130 -0.0020 -0.0010; -0.0020 0.0090 -0.0010; -0.0010 -0.0010 0.0110];
IC7 = [0.1510 -0.0020 -0.0030; -0.0020 0.1950 -0.0080; -0.0030 -0.0080 0.0960];
%% 3. 牛顿-欧拉动力学计算
Z = [0; 0; 1];

% ========= 连杆1 =========
W11 = R10*[0;0;0] + dq1*Z;
W_d11 = R10*[0;0;0] + cross(R10*[0;0;0], dq1*Z) + S1*Z;
V_d11 = R10*(cross([0;0;0],P01) + cross([0;0;0],cross([0;0;0],P01)) + [0;0;0]);
VC_d11 = cross(W_d11,PC1) + cross(W11,cross(W11,PC1)) + V_d11;
F11 = 6.34*VC_d11;
N11 = IC1*W_d11 + cross(W11,IC1*W11);
% ========= 连杆2 =========
W22 = R21*W11 + dq2*Z;
W_d22 = R21*W_d11 + cross(R21*W11,dq2*Z) + S2*Z;
V_d22 = R21*(cross(W_d11,P12) + cross(W11,cross(W11,P12)) + V_d11);
VC_d22 = cross(W_d22,PC2) + cross(W22,cross(W22,PC2)) + V_d22;
F22 = 6.32*VC_d22;
N22 = IC2*W_d22 + cross(W22,IC2*W22);
% ========= 连杆3 =========
W33 = R32*W22 + dq3*Z;
W_d33 = R32*W_d22 + cross(R32*W22,dq3*Z) + S3*Z;
V_d33 = R32*(cross(W_d22,P23) + cross(W22,cross(W22,P23)) + V_d22);
VC_d33 = cross(W_d33,PC3) + cross(W33,cross(W33,PC3)) + V_d33;
F33 = 7.93*VC_d33;
N33 = IC3*W_d33 + cross(W33,IC3*W33);
% ========= 连杆4 =========
W44 = R43*W33 + dq4*Z;
W_d44 = R43*W_d33 + cross(R43*W33,dq4*Z) + S4*Z;
V_d44 = R43*(cross(W_d33,P34) + cross(W33,cross(W33,P34)) + V_d33);
VC_d44 = cross(W_d44,PC4) + cross(W44,cross(W44,PC4)) + V_d44;
F44 = 5.67*VC_d44;
N44 = IC4*W_d44 + cross(W44,IC4*W44);
% ========= 连杆5 =========
W55 = R54*W44 + dq5*Z;
W_d55 = R54*W_d44 + cross(R54*W44,dq5*Z) + S5*Z;
V_d55 = R54*(cross(W_d44,P45) + cross(W44,cross(W44,P45)) + V_d44);
VC_d55 = cross(W_d55,PC5) + cross(W55,cross(W55,PC5)) + V_d55;
F55 = 2.84*VC_d55;
N55 = IC5*W_d55 + cross(W55,IC5*W55);
% ========= 连杆6 =========
W66 = R65*W55 + dq6*Z;
W_d66 = R65*W_d55 + cross(R65*W55,dq6*Z) + S6*Z;
V_d66 = R65*(cross(W_d55,P56) + cross(W55,cross(W55,P56)) + V_d55);
VC_d66 = cross(W_d66,PC6) + cross(W66,cross(W66,PC6)) + V_d66;
F66 = 2.71*VC_d66;
N66 = IC6*W_d66 + cross(W66,IC6*W66);
% ========= 连杆7 =========
W77 = R76*W66 + dq7*Z;
W_d77 = R76*W_d66 + cross(R76*W66,dq7*Z) + S7*Z;
V_d77 = R76*(cross(W_d66,P67) + cross(W66,cross(W66,P67)) + V_d66);
VC_d77 = cross(W_d77,PC7) + cross(W77,cross(W77,PC7)) + V_d77;
F77 = 12.08*VC_d77;
N77 = IC7*W_d77 + cross(W77,IC7*W77);
%% 4. 完整内推法计算（全部7个连杆）
% ========= 连杆7 =========
f7 = F77;
n7 = N77 + cross(PC7,F77);
t7 = n7'*Z;
% ========= 连杆6 =========
f6 = R67*f7 + F66;
n6 = N66 + R67*n7 + cross(PC6,F66) + cross(P67,R67*f7);
t6 = n6'*Z;
% ========= 连杆5 =========
f5 = R56*f6 + F55;
n5 = N55 + R56*n6 + cross(PC5,F55) + cross(P56,R56*f6);
t5 = n5'*Z;
% ========= 连杆4 =========
f4 = R45*f5 + F44;
n4 = N44 + R45*n5 + cross(PC4,F44) + cross(P45,R45*f5);
t4 = n4'*Z;
% ========= 连杆3 =========
f3 = R34*f4 + F33;
n3 = N33 + R34*n4 + cross(PC3,F33) + cross(P34,R34*f4);
t3 = n3'*Z;
% ========= 连杆2 =========
f2 = R23*f3 + F22;
n2 = N22 + R23*n3 + cross(PC2,F22) + cross(P23,R23*f3);
t2 = n2'*Z;
% ========= 连杆1 =========
f1 = R12*f2 + F11;
n1 = N11 + R12*n2 + cross(PC1,F11) + cross(P12,R12*f2);
t1 = n1'*Z;
% 最终关节力矩向量
y=[t1;t2;t3;t4;t5;t6;t7];

end

